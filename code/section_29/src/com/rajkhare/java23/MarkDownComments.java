package com.rajkhare.java23;

///  **This class contains methods to perform arithmetic operations**
///  @author Raj Khare
///  @version 1.0
public class MarkDownComments {

    ///  |Input| Output|
    ///  |------|-------|
    /// | 2,3    | 5 |
    /// | 9,2    | 11 |
    ///  | 25,75    | 100 |
    /// **This method adds two numbers**
    /// @param a first number
    /// @param b second number
    ///  @return sum of the two numbers
    public static int add(int a, int b) {
        return a + b;
    }

    ///  ## This method subtracts two numbers
    ///  - 5, 3 = 2
    ///  - 9, 2 = 7
    ///  @param a first number
    ///  @param b second number
    ///  @return ***substraction of the two numbers***
    public static int subtract(int a, int b) {
        return a - b;
    }

    ///  ---
    ///  # This method multiplies two numbers
    ///  @param a first number
    ///  @param b second number
    ///  @return ***multiplication of the two numbers***
    public static int multiply(int a, int b) {
        return a * b;
    }

    ///  # This method divides two numbers
    ///  ```
    /// @Override
    ///  public void division() ...
    /// ```
    ///  @param a first number
    ///  @param b second number
    ///  @return ***division of the two numbers***
    public static int divide(int a, int b) {
        return a / b;
    }
}
